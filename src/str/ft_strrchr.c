#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	len;

	len = ft_strlen(s);
	while (s[len] != c && len)
		len--;
	if (s[len] != c)
		return (NULL);
	return ((char*)s + len);
}
